const mongoose = require('mongoose');

// User schema for a simple demo app.
// Passwords are stored as plain text to keep this beginner-friendly.
// Do not use plain text passwords in production systems.
const userSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
    trim: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
    lowercase: true,
    trim: true,
  },
  password: {
    type: String,
    required: true,
  },
  createdAt: {
    type: Date,
    default: Date.now,
  },
});

module.exports = mongoose.model('User', userSchema);
